async function prepareAndSetDefaultTimeZoneOptions() {
    let defaultTimeZoneField = document.getElementById("Default-User-Time-Zone");
    for (let i = 0; i < DefaultUserTimeZoneOptions.length; i++) {
        let option = document.createElement("option");
        option.setAttribute("value", DefaultUserTimeZoneOptions[i][0]);
        option.innerText = DefaultUserTimeZoneOptions[i][1];
        defaultTimeZoneField.appendChild(option);
    }
    return Promise.resolve();
}